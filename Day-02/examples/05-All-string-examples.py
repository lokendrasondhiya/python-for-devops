str1 ="Lokendra"
str2 = "Sondhiya"

#concat
full_name = str1 + " " + str2
print("Full Name is:", full_name)

#len
length = len(full_name)
print("Length of Full Name is :", length)

#lower/upper case
lowercase = full_name.lower()
uppercase = full_name.upper()
print("Name in Lowercase is :",lowercase)
print("Name in Uppercase is :",uppercase)